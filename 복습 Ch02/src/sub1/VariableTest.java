package sub1;

public class VariableTest {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 7;
		a = 5;
		
		System.out.println("a : " + a);
		System.out.println("b : " + b);
		
		String name = "홍길동";
		System.out.println(name + "님 반갑습니다.");
		
		final int NUM = 5;
		System.out.println("NUM : " + NUM);
	}
}